package basicJava;

public class Methos_Operators_10_06 {
	static void addition() {
		int a = 20;
		int b = 10;
		
		System.out.println(a+b);
		System.out.println(b-a);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		
		
	}
	public static void main(String[] args) {
		addition();
		
	}

}
