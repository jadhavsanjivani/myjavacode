package basicJava;

public class Operator_If_Statement {
	public static void main(String[] args) {
		int a = 20;
		int b = 30;
		if(a>b) {
			System.out.println("a is greater than b");
			
		}
		if(a==b) {
			System.out.println("a is equals to b");
		}
		if(a<b) {
			System.out.println("a is less than b");
		}
	}
}
