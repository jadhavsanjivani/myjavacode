package concept_inheritance;
class Super_Class
{
	static void mansion()
	{
		System.out.println("Have Mansion");
	}
	void big_Car()
	{
		System.out.println("Have Big Car");
	}
}

public class Parent_Class extends Super_Class
{
	static void home()
	{
		System.out.println("Have Home");
	}
	void car()
	{
		System.out.println("Have Car");
	}
}
	
	
